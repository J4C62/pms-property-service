/**
 * Enum representing the layout types available for a property.
 *
 * <p>This enumeration lists all possible layout configurations, such as open space, separated rooms,
 * closed, and multi-unit layouts. It is used to describe the spatial organization of a property.
 *
 * @author Jose Antonio (J4c62)
 * @version 1.0.0
 * @since 2025-09-26
 */
public enum LayoutType {
  OPEN_SPACE,
  SEPARATED_ROOMS,
  CLOSED,
  MULTI_UNIT
}
